import os
import sys
import glob
import pandas as pd

# Add src to path to import config
sys.path.append(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
import config
from features.technical_analysis import feature_engineer

def build_master_dataset():
    """
    Reads all raw CSVs, applies technical indicators and labels,
    and consolidates them into a single Parquet file.
    """
    raw_files = glob.glob(os.path.join(config.RAW_DATA_DIR, "*.csv"))
    print(f"Ditemukan {len(raw_files)} data mentah. Memulai proses fitur dan pelabelan...")
    
    all_data = []
    
    for i, filepath in enumerate(raw_files):
        # Extract ticker from filename (e.g., 'BBCA.JK_5y_1d.csv' -> 'BBCA.JK')
        filename = os.path.basename(filepath)
        ticker = filename.split('_')[0]
        
        try:
            df = feature_engineer(filepath)
            
            if df.empty or len(df) < 50:
                continue
                
            # Drop NaN rows generated by rolling windows and shifting
            df = df.dropna()
            
            if df.empty:
                continue
                
            # Add ticker column identifying the stock
            df['Ticker'] = ticker
            
            # Reset index if the date is the index, to keep Date as a column
            if isinstance(df.index, pd.DatetimeIndex):
                df = df.reset_index()
                df.rename(columns={'index': 'Date', 'Date': 'Date'}, inplace=True)
                
            all_data.append(df)
            
            if (i + 1) % 50 == 0:
                print(f"Memproses {i + 1}/{len(raw_files)} emiten...")
                
        except Exception as e:
            print(f"Error processing {ticker}: {e}")
            
    if not all_data:
        print("Dataset kosong. Pastikan data raw tersedia.")
        return
        
    master_df = pd.concat(all_data, ignore_index=True)
    
    # Save to Parquet
    out_path = os.path.join(config.PROCESSED_DATA_DIR, "master_dataset.parquet")
    
    # Needs pyarrow installed to export to parquet
    master_df.to_parquet(out_path, engine='pyarrow', index=False)
    
    # Calculate some statistics
    total_rows = len(master_df)
    target_1 = master_df['Target_Momentum'].sum()
    target_0 = total_rows - target_1
    win_p = (target_1 / total_rows) * 100
    
    print(f"\n==========================================")
    print(f"Master Dataset Berhasil Dibuat!")
    print(f"Lokasi: {out_path}")
    print(f"Total Baris Data: {total_rows:,}")
    print(f"Distribusi Kelas (ARA/Momentum):")
    print(f"  Target 0 (Tidak sukses): {target_0:,.0f} baris")
    print(f"  Target 1 (Sukses >5%): {target_1:,.0f} baris ({win_p:.2f}%)")
    print(f"==========================================\n")

if __name__ == "__main__":
    build_master_dataset()
